#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *src;

	src = (unsigned char*)s;
	while (n)
	{
		if ((unsigned char)c == *src)
			return (src);
		src++;
		n--;
	}
	return (NULL);
}
