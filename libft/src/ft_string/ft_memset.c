#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *ptr;

	ptr = (unsigned char*)b;
	while (len)
	{
		*ptr = (char)c;
		ptr++;
		len--;
	}
	return (b);
}
