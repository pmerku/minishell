#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (*s)
	{
		if ((unsigned char)c == *s)
			return ((char*)s);
		s++;
	}
	if (*s == '\0' && (unsigned char)c == *s)
		return ((char*)s);
	return (NULL);
}
