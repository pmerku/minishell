#include <ft_stdio/ft_printf_utils.h>

void	buf_fd_putchars(void *raw, char c, size_t n)
{
	size_t			i;

	i = 0;
	while (i < n)
	{
		buf_fd_putchar(raw, c);
		i++;
	}
}
